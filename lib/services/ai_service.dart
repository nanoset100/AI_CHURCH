  /// AI 설교 생성
  /// 
  /// [userPrompt] 사용자의 고민 키워드
  /// [delaySeconds] 딜레이 시간 (3-5초)
  /// 
  /// Returns: 생성된 Sermon 객체
  static Future<Sermon> generateSermon({
    required String userPrompt,
    int delaySeconds = 3,
  }) async {
    // 3-5초 딜레이 (AI가 생각하는 듯한 UX)
    await Future.delayed(Duration(seconds: delaySeconds));

    // 고민 키워드 매칭
    Map<String, String>? matchedSermon;
    
    for (final entry in _sampleSermons.entries) {
      final keyword = entry.key;
      // 키워드가 포함되어 있거나, 입력의 일부가 키워드에 포함되면 매칭
      if (userPrompt.contains(keyword) || 
          keyword.contains(userPrompt.substring(0, userPrompt.length > 10 ? 10 : userPrompt.length))) {
        matchedSermon = entry.value;
        break;
      }
    }

    // 매칭된 설교가 없으면 기본 설교 사용
    final sermonData = matchedSermon ?? _defaultSermon;

    // Sermon 객체 생성
    return Sermon(
      id: DateTime.now().millisecondsSinceEpoch.toString(),
      title: sermonData['title']!,
      verse: sermonData['verse']!,
      content: sermonData['content']!,
      userPrompt: userPrompt,
      createdAt: DateTime.now(),
    );
  }

